    /**
     * Найти координаты по названию (нечёткое совпадение по первому слову/подстроке).
     */
    public function lookup(string $query): ?GeoPoint
    {
        $q = Str::lower(trim($query));
        if ($q === '') {
            return null;
        }

        // Точное совпадение
        if (isset(self::CITIES[$q])) {
            return $this->point($q);
        }

        // Совпадение по первому токену (до запятой/пробела): "Москва, ул. Ленина" → "москва"
        $firstToken = Str::lower(trim(preg_split('/[,;]/', $q)[0] ?? ''));
        if (isset(self::CITIES[$firstToken])) {
            return $this->point($firstToken);
        }

        // Подстрочный поиск: любой город, входящий в запрос
        foreach (self::CITIES as $name => $coords) {
            if (Str::contains($q, $name)) {
                return new GeoPoint(lat: $coords[0], lng: $coords[1], label: Str::title($name));
            }
        }

        return null;
    }

    private function point(string $key): GeoPoint
    {
        [$lat, $lng] = self::CITIES[$key];
        return new GeoPoint(lat: $lat, lng: $lng, label: Str::title($key));
    }
